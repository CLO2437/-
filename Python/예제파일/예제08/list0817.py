# 두 문자열을 집합으로 변환하여 집합 연산하기

set1 = set(input('문자열 s1：'))
set2 = set(input('문자열 s2：'))

print('set1：', set1)
print('set2：', set2)
print()
print('set1 == set2：', set1 == set2)
print('set1 != set2：', set1 != set2)
print('set1 <  set2：', set1 <  set2)
print('set1 <= set2：', set1 <= set2)
print('set1 >  set2：', set1 >  set2)
print('set1 >= set2：', set1 >= set2)
print()
print('set1 | set2：', set1 | set2)
print('set1 & set2：', set1 & set2)
print('set1 - set2：', set1 - set2)
print('set1 ^ set2：', set1 ^ set2)
