# bool타입 값 출력하기

a = int(input('정수a：'))
b = int(input('정수b：'))

print('a <  b ：', a <  b)
print('a <= b ：', a <= b)
print('a >  b ：', a >  b)
print('a >= b ：', a >= b)
print('a == b ：', a == b)
print('a != b ：', a != b)

print('False   ：', False)
print('True    ：', True)
print('True + 5：', True + 5)  # 1 + 5로 처리됩니다.
